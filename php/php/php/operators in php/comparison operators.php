<?php

/*
 "Comparison Operators"
 1. ==  equal
 2. <>  not equal
 3. >   greater than
 4. <   less than
 5. >=  greater than equal
 6. <=  less than equal
*/


$a = 2;
$b = 3;

// == equal
echo var_dump($a == $b);

echo "</br>";

//  <> not equal
echo var_dump($a <> $b);

echo "</br>";

// > greater than 
echo var_dump($a > $b);

echo "</br>";

// < less than 
echo var_dump($a < $b);

echo "</br>";

// >= greater than equal
echo var_dump($a >= $b);

echo "</br>";

// <= greater than 
echo var_dump($a <= $b);

?>